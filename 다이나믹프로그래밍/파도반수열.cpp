#include <bits/stdc++.h>

using namespace std;

int testCase;
unsigned long long d[101] = {0, 1, 1, 1};
vector<unsigned long long> result;
unsigned long long dp(int x)
{
	if(x <= 3) return 1;
	if(d[x] != 0) return d[x];
	return d[x] = dp(x - 2) + dp(x - 3);
}

int main(void)
{
	cin >> testCase;
	for(int i = 0; i < testCase; i++)
	{
		int x;
		cin >> x;
		result.push_back(dp(x));
	}
	
	for(int i = 0; i < result.size(); i++)
	{
		cout << result[i] << endl;
	}
}
